-- FUNCTION: SET RANDOM NPC MODEL RANGE

DELIMITER //
DROP FUNCTION IF EXISTS ModelPick;
CREATE FUNCTION ModelPick(modelStart INT, modelEnd INT) RETURNS INT DETERMINISTIC
BEGIN	
	DECLARE model_pick INT;
	SELECT modelid1
	INTO model_pick
	FROM npc_creature_model
	WHERE modelid1 > modelStart AND modelid1 < modelEnd
	ORDER BY RAND() 
	LIMIT 1;
	RETURN model_pick;
END //

